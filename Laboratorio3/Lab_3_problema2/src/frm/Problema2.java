        int seleccion = fileChooser.showOpenDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            archivo = new Archivo(selectedFile.getAbsolutePath());
            int cantidadPalabras = archivo.contarPalabras();
            CantidaddePalabras.setText(String.valueOf(cantidadPalabras));
        }

