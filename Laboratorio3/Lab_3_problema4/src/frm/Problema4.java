    private void mostrarBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarBuscarActionPerformed
        // TODO add your handling code here:
    String usuario = JOptionPane.showInputDialog(this, "Ingrese el usuario a buscar:");
    if (usuario != null && !usuario.isEmpty()) {
        try {
            Usuario usuarioEncontrado = Usuario.buscar(usuario);
            DefaultTableModel model = (DefaultTableModel) tablausuarios.getModel();
            model.setRowCount(0); // Limpiar filas existentes
            
            if (usuarioEncontrado != null) {
                model.addRow(new Object[]{usuarioEncontrado.getUsuario(), usuarioEncontrado.getNombre(), usuarioEncontrado.getCorreo()});
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado", "Resultado de B√∫squeda", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_mostrarBuscarActionPerformed

    private void mostrarValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarValidarActionPerformed
        // TODO add your handling code here:
                String usuario = JOptionPane.showInputDialog(this, "Ingrese el usuario:");
        String password = JOptionPane.showInputDialog(this, "Ingrese la contrase√±a:");
        if (usuario != null && !usuario.isEmpty() && password != null && !password.isEmpty()) {
            try {
                boolean valido = Usuario.validar_usuario(usuario, password);
                if (valido) {
                    JOptionPane.showMessageDialog(this, "Usuario y contrase√±a v√°lidos", "Validaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al leer el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_mostrarValidarActionPerformed

    private void insertarusuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarusuarioActionPerformed
        // TODO add your handling code here:
                String usuario = jTextField1.getText();
        String password = jTextField2.getText();
        String nombre = jTextField3.getText();
        String correo = jTextField4.getText();
        
        if (!usuario.isEmpty() && !password.isEmpty() && !nombre.isEmpty() && !correo.isEmpty()) {
            Usuario nuevoUsuario = new Usuario(usuario, password, nombre, correo);
            try {
                nuevoUsuario.insertar();
                JOptionPane.showMessageDialog(this, "Usuario insertado correctamente", "Inserci√≥n", JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al escribir en el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_insertarusuarioActionPerformed

        
    
