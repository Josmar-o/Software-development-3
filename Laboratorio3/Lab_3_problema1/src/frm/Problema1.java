    private void AgregarNombresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarNombresActionPerformed
        // TODO add your handling code here:
        String nombre = name.getText();
        if(!nombre.isEmpty()) {
            try{
                archivo.agregarNombre(nombre);
                JOptionPane.showMessageDialog(this, "Nombre agregado correctamente.");name.setText("");} 
            catch(IOException ex) {
                JOptionPane.showMessageDialog(this,"Error al agregar el nombre.");
            }
        } else{
            JOptionPane.showMessageDialog(this,"Ingrese un nombre v√°lido.");}
    }//GEN-LAST:event_AgregarNombresActionPerformed

    private void MostarNombresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostarNombresActionPerformed
        // TODO add your handling code here:
                                             
    try {
        String nombres = archivo.leerNombres();
        String[] lineas = nombres.split("\n");
        
        DefaultTableModel model = (DefaultTableModel) tablanombres.getModel();
        model.setRowCount(0); // Limpiar filas existentes
        
        for (String linea : lineas) {
            model.addRow(new Object[]{linea});
        }
        
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error al leer los nombres.");
    }

    }//GEN-LAST:event_MostarNombresActionPerformed

    private void MostrarCarpetadeArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarCarpetadeArchivoActionPerformed
        // TODO add your handling code here:
    try {
        Desktop.getDesktop().open(new File(archivo.getRutaArchivo()).getParentFile());
    }catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error al abrir la carpeta del archivo.");
    }
    }//GEN-LAST:event_MostrarCarpetadeArchivoActionPerformed

