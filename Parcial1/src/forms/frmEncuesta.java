    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        String SO = "", interes = "";
        if(radLinux.isSelected()){
            SO = " Linux ";
        }else if(radMac.isSelected()){
            SO = " Mac ";
        }else if(radWindows.isSelected()){
            SO = " Windows ";
        }
        if(checkAdm.isSelected()){
            interes = " Administracion ";
        }if(checkDiseno.isSelected()){
            interes += " Diseño Grafico ";
        }if(checkProgramacion.isSelected()){
            interes += " Programacion ";
        }
        JOptionPane.showMessageDialog(rootPane, "Tu sistema operativo preferido es:"+ SO+ ", tus especialidades son: " + interes + " y el numero de horas dedicadas son: "+ lblSlider.getText());
    }//GEN-LAST:event_btnGenerarActionPerformed

