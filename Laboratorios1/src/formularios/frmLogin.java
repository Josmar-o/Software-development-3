    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        if(txtUser.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor introducir el usuario");
        }else if(txtPass.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor introducir la contraseña");
        }else{
            if(Usuario.validateUser(txtUser.getText(), txtPass.getText(), usuarios) == true){
                frmMenu frmMenu = new frmMenu();
                frmMenu.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Contraseña o Usuario incorrectos");
            }
        }
        
        
    }//GEN-LAST:event_loginActionPerformed

    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        if(txtUser.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor introducir el usuario");
        }else if(txtPass.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor introducir la contraseña");
        }else{
            Usuario.registerUser(txtUser.getText(), txtPass.getText(), usuarios);
            JOptionPane.showMessageDialog(rootPane, "Resgistro exitoso");
        }
        
    }//GEN-LAST:event_registerActionPerformed

