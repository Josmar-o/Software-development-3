    private void cargarRegitros(JTable table){
        DefaultTableModel modelo = (DefaultTableModel) table.getModel();
        modelo.setRowCount(0);
        ArrayList <Empleado> listaEmpleados = MetodosEmpleados.cargarEmpleados();
        
        for(int i = 0; i < listaEmpleados.size(); i++){
            Empleado empleado = listaEmpleados.get(i);
            String [] arreglo = {empleado.getCedula(),String.valueOf(empleado.getFechaNacimiento()), empleado.getNombre(), empleado.getNombre2(), empleado.getApellido(), empleado.getApellido2(), empleado.getTelefono(), empleado.getDireccion()};
            modelo.addRow(arreglo);  
          }
    }
